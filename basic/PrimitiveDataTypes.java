package com.basic;


public class PrimitiveDataTypes {
	
	public static void main(String[] args) {
		byte b = 2;
		System.out.println(b);
		System.out.println(Byte.MAX_VALUE);
		System.out.println(Byte.MIN_VALUE);
		
		System.out.println("The Maximum Value Of Integer is:"+Integer.MIN_VALUE);
		System.out.println("The Minumum Value Of Integer is:"+Integer.MAX_VALUE);
		System.out.println(" ");

		
		
		System.out.println("The Maximum Value Of float is:"+Float.MIN_VALUE);
		System.out.println("The Minumum Value Of float is:"+Float.MAX_VALUE);
		System.out.println(" ");
		
		System.out.println("The Maximum Value Of float is:"+Double.MIN_VALUE);
		System.out.println("The Minumum Value Of float is:"+Double.MAX_VALUE);	
		
	}

}